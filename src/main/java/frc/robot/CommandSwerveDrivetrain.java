/**
 * Class that extends the Phoenix SwerveDrivetrain class and implements
 * subsystem
 * so it can be used in command-based projects easily.
 */
public class CommandSwerveDrivetrain extends SwerveDrivetrain implements Subsystem {
    // This data store information on update speed
    private static final double kSimLoopPeriod = 0.005; // 5 ms
    private Notifier m_simNotifier = null;
    private double m_lastSimTime;
    public final SwerveRequest.ApplyChassisSpeeds autoRequest = new SwerveRequest.ApplyChassisSpeeds();
    private final CurrentLimitsConfigs driveCurrentLimit = new CurrentLimitsConfigs()
            .withSupplyCurrentLimitEnable(true)
            .withSupplyCurrentLimit(60);
    private final CurrentLimitsConfigs steerCurrentLimit = new CurrentLimitsConfigs()
            .withSupplyCurrentLimitEnable(true)
            .withSupplyCurrentLimit(20);

    // There are 2 constructors based on what you pass in
    public CommandSwerveDrivetrain(Supplier<Boolean> teamColor, SwerveDrivetrainConstants driveTrainConstants,
            double OdometryUpdateFrequency,
            SwerveModuleConstants... modules) {
        super(driveTrainConstants, OdometryUpdateFrequency, modules);
        configurePathPlanner(teamColor);
        if (Utils.isSimulation()) {
            startSimThread();
        }
        // for (SwerveModule module : Modules) {
        //     module.getDriveMotor().getConfigurator().apply(driveCurrentLimit);
        //     module.getSteerMotor().getConfigurator().apply(steerCurrentLimit);
        // }
    }

    // The second one is the one we use
    public CommandSwerveDrivetrain(Supplier<Boolean> teamColor, SwerveDrivetrainConstants driveTrainConstants,
            SwerveModuleConstants... modules) {
        super(driveTrainConstants, modules);

        configurePathPlanner(teamColor); // Loads the path planner config makes sure this is run before and other path
        // planner stuff
        if (Utils.isSimulation()) {
            startSimThread();
        }
        // for (SwerveModule module : Modules) {
        //     module.getDriveMotor().getConfigurator().apply(driveCurrentLimit);
        //     module.getSteerMotor().getConfigurator().apply(steerCurrentLimit);
        // }
    }

    public SwerveDrivePoseEstimator getOdometry() {
        return m_odometry;
    }

    public boolean firstFail = true;
    public Timer failTimer = new Timer();

    public void setOdometryVisionMeasurement(Pose2d estimatedCamPose, double camTimestampSecs) {
        // Was getting error that would stop the code and crash it. Adding a try and
        // chat statement solved the problem
        try {
            if (!firstFail) {
                failTimer.stop();
            }
            m_stateLock.writeLock().lock();
            m_odometry.addVisionMeasurement(estimatedCamPose, camTimestampSecs);
        } catch (Exception ex) {
            if (firstFail) {
                failTimer.start();
                firstFail = false;
            }
            SmartDashboard.putNumber("Failing time", failTimer.get());
        } finally {
            m_stateLock.writeLock().unlock();
        }
    }

    private void configurePathPlanner(Supplier<Boolean> teamColor) {
        double driveBaseRadius = 0;
        for (var moduleLocation : m_moduleLocations) {
            driveBaseRadius = Math.max(driveBaseRadius, moduleLocation.getNorm());
        }
        // Path planner config stuff
        AutoBuilder.configureHolonomic(
                () -> this.getState().Pose, // Supplier of current robot pose
                this::seedFieldRelative, // Consumer for seeding pose against auto
                this::getCurrentRobotChassisSpeeds,
                (speeds) -> this.setControl(autoRequest.withSpeeds(speeds)), // Consumer of ChassisSpeeds to drive the
                                                                             // robot
                new HolonomicPathFollowerConfig(new PIDConstants(8.5, 0, 0), new PIDConstants(3.5, 0, 0),
                        TunerConstants.kSpeedAt12VoltsMps,
                        driveBaseRadius,
                        new ReplanningConfig()),
                () -> teamColor.get(), // Change this if the path needs to be flipped on red vs blue
                this); // Subsystem for requirements
    }

    public Command applyRequest(Supplier<SwerveRequest> requestSupplier) {
        return run(() -> this.setControl(requestSupplier.get()));
    }

    // Return a path as a command for auto or teleop
    public Command getAutoPath(String pathName) {
        return new PathPlannerAuto(pathName);
    }

    public Command getPath(String pathName) {
        PathPlannerPath path = PathPlannerPath.fromPathFile(pathName);
        return AutoBuilder.followPath(path);
    }

    public Pose2d getStartingPath(String pathName) {
        PathPlannerPath path = PathPlannerPath.fromPathFile(pathName);
        return path.getPreviewStartingHolonomicPose();
    }

    public ChassisSpeeds getCurrentRobotChassisSpeeds() {
        return m_kinematics.toChassisSpeeds(getState().ModuleStates);
    }

    private void startSimThread() {
        m_lastSimTime = Utils.getCurrentTimeSeconds();

        /* Run simulation at a faster rate so PID gains behave more reasonably */
        m_simNotifier = new Notifier(() -> {
            final double currentTime = Utils.getCurrentTimeSeconds();
            double deltaTime = currentTime - m_lastSimTime;
            m_lastSimTime = currentTime;

            /* use the measured time delta, get battery voltage from WPILib */
            updateSimState(deltaTime, RobotController.getBatteryVoltage());
        });
        m_simNotifier.startPeriodic(kSimLoopPeriod);
    }
}
